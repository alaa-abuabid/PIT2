package com.example.alaa_ab.pit2;


//class for a point, has cordinations x,y and has the id of it's imageview

public class Point {
    private int x ;
    private int y ;
    private int myImageId;

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getMyImageId() {
        return myImageId;
    }

    public void setMyImageId(int myImageId) {
        this.myImageId = myImageId;
    }
}
